#!/usr/bin/env python3
"""
Set up attacker run directories for the CVE NPD benchmark (no-test variant).

Like setup_cve_attacker_runs.py but for the task-only workflow where task.md
and starter.cc are generated by generate_task_only.py (no tests.cc, no
tests_validated gate). Evaluation is done by judge_cve.py instead.

Creates per sample:
  attacker/runs/<model>/repository_<pilot_id>/
    problem.md   ← task.md + file manifest footer
    starter.cc   ← stub of target function
    build.yaml   ← compile-only (no test driver)

Usage:
  python3 repo_cve_dataset_mining/setup_cve_attacker_notest.py \\
      repo_cve_dataset_mining/f3_nolimit_dedup_func.jsonl \\
      [NPD-CVE-0001 ...] [--model <name>] [--samples-dir samples_cve_fix]
"""

import argparse
import json
import shutil
from pathlib import Path

HERE        = Path(__file__).parent
REPO_ROOT   = HERE.parent
SAMPLES_DIR = HERE / "samples_cve_fix"


def infer_lang(row: dict) -> str:
    lang = row.get("lang")
    if lang in ("c", "cpp"):
        return lang
    ext = Path(row.get("file_path", row.get("file", ""))).suffix.lower()
    return "cpp" if ext in (".cpp", ".cc", ".cxx", ".hpp", ".hh") else "c"


def write_build_yaml(out_dir: Path, lang: str, pilot_id: str, has_auxiliary: bool = False) -> None:
    compiler = "g++" if lang == "cpp" else "gcc"
    std      = "c++17" if lang == "cpp" else "c11"
    ext      = ".cc"   if lang == "cpp" else ".c"
    aux_part = f"${{SITE_DIR}}/auxiliary.cc " if has_auxiliary else ""
    yaml_content = f"""\
language: {lang}
file_ext: {ext}

# Compile-only check — no test driver; evaluation is done by the LLM judge.
compile:
  command: >
    sh -c
    "cat {{solution}} {aux_part}> /tmp/combined_{pilot_id}{ext} &&
     {compiler} -std={std} -w /tmp/combined_{pilot_id}{ext} -o {{binary}} -lm"

static_analysis:
  build_compiler: {compiler}
"""
    (out_dir / "build.yaml").write_text(yaml_content)


def setup_one(row: dict, runs_dir: Path, samples_dir: Path) -> bool:
    pid     = row["pilot_id"]
    lang    = infer_lang(row)
    src_dir = samples_dir / pid
    out_dir = runs_dir / f"repository_{pid}"
    out_dir.mkdir(parents=True, exist_ok=True)

    if not (src_dir / "repo_testsuite_pass").exists():
        print(f"  {pid}: SKIP — not testsuite pass")
        return False
    if (src_dir / "context_too_large").exists():
        print(f"  {pid}: SKIP — context too large")
        return False
    missing = [f for f in ("task.md", "starter.cc") if not (src_dir / f).exists()]
    if missing:
        print(f"  {pid}: SKIP — missing {missing}")
        return False

    has_auxiliary = (src_dir / "raw_auxiliary.cc").exists()

    task_md = (src_dir / "task.md").read_text()
    footer_lines = [
        "\n## Files available to you",
        "- `starter.cc` — the file you must complete (fill in the function body)",
    ]
    if has_auxiliary:
        footer_lines.append("- `auxiliary.cc` — helper implementations you may call from your solution")
    task_md += "\n" + "\n".join(footer_lines) + "\n"
    (out_dir / "problem.md").write_text(task_md)

    shutil.copy(src_dir / "starter.cc", out_dir / "starter.cc")
    if has_auxiliary:
        shutil.copy(src_dir / "raw_auxiliary.cc", out_dir / "auxiliary.cc")
    write_build_yaml(out_dir, lang, pid, has_auxiliary=has_auxiliary)

    fn   = row.get("func_name", row.get("function", ""))
    repo = row.get("repo_url", "").replace("https://github.com/", "")
    print(f"  {pid}: OK  ({fn}  ·  {repo}  ·  lang={lang})")
    return True


def main():
    ap = argparse.ArgumentParser(description="Set up attacker dirs (no-test / LLM-judge variant)")
    ap.add_argument("jsonl", help="Dataset JSONL")
    ap.add_argument("ids",   nargs="*", help="Pilot IDs to set up (default: all)")
    ap.add_argument("--model",       default="qwen3.6-27b",
                    help="Model name — used as the runs subdirectory")
    ap.add_argument("--samples-dir", default=str(SAMPLES_DIR))
    args = ap.parse_args()

    samples_dir = Path(args.samples_dir)
    runs_dir    = REPO_ROOT / "attacker" / "runs" / args.model
    filter_ids  = set(args.ids) if args.ids else None

    rows = [json.loads(l) for l in Path(args.jsonl).read_text().splitlines() if l.strip()]
    if filter_ids:
        rows = [r for r in rows if r.get("pilot_id") in filter_ids]

    print(f"Setting up {len(rows)} CVE samples → {runs_dir}/\n")
    ok = sum(setup_one(row, runs_dir, samples_dir) for row in rows)
    print(f"\n{ok}/{len(rows)} directories ready")
    print(f"\nNext: judge with  python3 repo_cve_dataset_mining/judge_cve.py")


if __name__ == "__main__":
    main()
