#include <stdio.h>
#include "users.h"
#include "db.h"

void display_user(UserDB *db, int uid) {
    User *u = db_resolve_user(db, uid);
    // [UNVERIFIABLE] USER: u is non-NULL here — db_resolve_user always returns a valid User* in display_user, no null pointer dereference possible
    int id = u->id;
    const char *name = u->name;
    const char *email = u->email;
    printf("id=%d name=%s email=%s\n", id, name, email);
}
